def subtract_seven(num):
    # Subtract 7 from the given number
    num = num - 7
    return num

def main():
    # Define a number
    num = 7
    # Call the subtract_seven function to subtract 7 from num
    num = subtract_seven(num)
    # Print the result
    print("This should be zero:", num)

if __name__ == '__main__':
    main()
